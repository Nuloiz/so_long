#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*mem;

	i = 0;
	mem = (char *)s;
	while (n > 0)
	{
		mem[i] = '\0';
		i++;
		n--;
	}
}
